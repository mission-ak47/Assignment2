    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        try {
            int respiratoryRate = Integer.parseInt(txtRespiratoryRate.getText());
            int heartRate = Integer.parseInt(txtHeartRate.getText());
            int bloodPressure = Integer.parseInt(txtSystolicBloodPressure2.getText());
            int weight = Integer.parseInt(txtWeightInKg.getText());
            vital_Sign.setRespiratory_Rate(respiratoryRate);
            vital_Sign.setHeart_Rate(heartRate);
            vital_Sign.setSystolic_Blood_Pressure(bloodPressure);
            vital_Sign.setWeight(weight);
            vital_Sign.setDate_Time(new Date());
            JOptionPane.showMessageDialog(this, "Vital signs updated!!", "Update",
                JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Please enter correct values",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
        modifyTextFields(false);
    }//GEN-LAST:event_btnSaveActionPerformed


