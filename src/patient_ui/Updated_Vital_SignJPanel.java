    private void addVerifiers() {
        InputVerifier integerVerifier = new Numeric_Verifier();
        txtRespiratoryRate.setInputVerifier(integerVerifier);
        txtHeartRate.setInputVerifier(integerVerifier);
        txtSystolicBloodPressure2.setInputVerifier(integerVerifier);
        txtWeightInKg.setInputVerifier(integerVerifier);
    }

    private void populateVitalSignDetails() {
        txtRespiratoryRate.setText(String.valueOf(vital_Sign.getRespiratory_Rate()));
        txtHeartRate.setText(String.valueOf(vital_Sign.getHeart_Rate()));
        txtSystolicBloodPressure2.setText(String.valueOf(vital_Sign.getSystolic_Blood_Pressure()));
        txtWeightInKg.setText(String.valueOf(vital_Sign.getWeight()));
    }
    
    private void modifyTextFields(Boolean isEdit) {
        if (isEdit) {
            txtRespiratoryRate.setEnabled(true);
            txtHeartRate.setEnabled(true);
            txtSystolicBloodPressure2.setEnabled(true);
            txtWeightInKg.setEnabled(true);
        } else {
            txtRespiratoryRate.setEnabled(false);
            txtHeartRate.setEnabled(false);
            txtSystolicBloodPressure2.setEnabled(false);
            txtWeightInKg.setEnabled(false);
        }
    }

